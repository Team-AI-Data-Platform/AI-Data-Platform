######################################################
# build_context()
######################################################

# - 기능 및 역할:
#
#   Vector DB에서 검색된 여러 개의 문서 검색 결과를
#   LLM이 참고할 수 있는 하나의 Context 문자열로 변환한다.
#
#   RAG에서는 LLM에게 질문만 전달하는 것이 아니라,
#   질문과 관련된 참고 문서도 함께 전달한다.
#
#   이 함수는 그 참고 문서 영역을 만드는 역할을 한다.
#
# - 파라미터:
#
#   search_results: List[Dict]
#
#   검색 결과 목록이다.
#
#   예)
#   [
#       {
#           "rank": 1,
#           "document": "MicroServer 프레임워크는 ...",
#           "metadata": {"source": "guide.md"},
#           "distance": 0.12
#       },
#       {
#           "rank": 2,
#           "document": "주요 구성요소는 ...",
#           "metadata": {"source": "guide.md"},
#           "distance": 0.18
#       }
#   ]
#
# - 리턴 타입:
#
#   str
#
# - 리턴값:
#
#   여러 검색 문서를 하나로 합친 문자열이다.
#
#   예)
#   [문서 1]
#   MicroServer 프레임워크는 ...
#
#   [문서 2]
#   주요 구성요소는 ...
def build_context(search_results: List[Dict]) -> str:
    """검색 결과를 LLM이 참고할 Context 문자열로 변환한다."""

    ######################################################
    # context_parts
    ######################################################

    # - 기능 및 역할:
    #
    #   검색 결과 문서들을 하나씩 문자열로 만든 뒤
    #   임시로 저장하는 리스트이다.
    #
    #   나중에 join()을 사용하여
    #   이 리스트 안의 문자열들을 하나의 큰 문자열로 합친다.
    #
    # - 타입:
    #
    #   list[str]
    #
    # - 초기값:
    #
    #   빈 리스트 []
    context_parts = []

    ######################################################
    # for 반복문
    ######################################################

    # - 기능 및 역할:
    #
    #   search_results 리스트 안에 들어 있는 검색 결과를
    #   하나씩 꺼내면서 반복 처리한다.
    #
    # - 반복 변수:
    #
    #   result
    #
    #   search_results 안의 검색 결과 1건을 의미한다.
    #
    # - 반복 예시:
    #
    #   첫 번째 반복 result:
    #   {
    #       "rank": 1,
    #       "document": "문서1 내용"
    #   }
    #
    #   두 번째 반복 result:
    #   {
    #       "rank": 2,
    #       "document": "문서2 내용"
    #   }
    for result in search_results:

        ######################################################
        # context_parts.append()
        ######################################################

        # - 기능 및 역할:
        #
        #   리스트의 마지막에 새로운 문자열을 추가한다.
        #
        #   여기서는 검색 결과 1건을 아래 형태의 문자열로 만들어 추가한다.
        #
        #   [문서 1]
        #   문서 내용
        #
        # - 파라미터:
        #
        #   f"[문서 {result['rank']}]\n{result['document']}"
        #
        #   리스트에 추가할 문자열이다.
        #
        # - 리턴 타입:
        #
        #   None
        #
        # - 리턴값:
        #
        #   append()는 값을 반환하지 않는다.
        #   대신 context_parts 리스트 자체가 변경된다.
        #
        # - f-string 설명:
        #
        #   문자열 앞에 f를 붙이면
        #   중괄호 {} 안에 변수나 표현식을 넣을 수 있다.
        #
        #   예)
        #   result['rank'] 값이 1이면
        #   f"[문서 {result['rank']}]" 결과는 "[문서 1]" 이 된다.
        #
        # - \n 의미:
        #
        #   줄바꿈 문자이다.
        #
        #   "[문서 1]\n문서 내용" 은 실제 출력 시 아래처럼 보인다.
        #
        #   [문서 1]
        #   문서 내용
        context_parts.append(
            f"[문서 {result['rank']}]\n{result['document']}"
        )

    ######################################################
    # join()
    ######################################################

    # - 기능 및 역할:
    #
    #   리스트 안의 문자열들을 하나의 문자열로 합친다.
    #
    #   여기서는 문서와 문서 사이에 빈 줄을 넣기 위해
    #   구분자로 "\n\n"을 사용한다.
    #
    # - 파라미터:
    #
    #   context_parts
    #
    #   합칠 문자열들이 들어 있는 리스트이다.
    #
    # - 리턴 타입:
    #
    #   str
    #
    # - 리턴값:
    #
    #   context_parts 안의 문자열들이 "\n\n" 기준으로 연결된 하나의 문자열이다.
    #
    # - 예시:
    #
    #   context_parts = [
    #       "[문서 1]\n문서1 내용",
    #       "[문서 2]\n문서2 내용"
    #   ]
    #
    #   "\n\n".join(context_parts)
    #
    #   결과:
    #
    #   [문서 1]
    #   문서1 내용
    #
    #   [문서 2]
    #   문서2 내용
    return "\n\n".join(context_parts)


######################################################
# build_prompt()
######################################################

# - 기능 및 역할:
#
#   사용자 질문과 Vector DB 검색 결과를 결합하여
#   LLM에게 전달할 최종 Prompt 문자열을 생성한다.
#
#   RAG 구조에서는 일반적으로 다음과 같은 정보를 Prompt에 포함한다.
#
#   1. AI의 역할
#   2. 답변 규칙
#   3. 참고 문서 Context
#   4. 사용자 질문
#   5. 답변 시작 위치
#
# - 파라미터:
#
#   1. question: str
#
#      사용자가 입력한 질문 문자열이다.
#
#      예)
#      "MicroServer 프레임워크의 주요 구성요소는 무엇인가?"
#
#   2. search_results: List[Dict]
#
#      Vector DB에서 검색된 문서 목록이다.
#
#      build_context() 함수의 입력값으로 사용된다.
#
# - 리턴 타입:
#
#   str
#
# - 리턴값:
#
#   LLM에게 전달할 최종 Prompt 문자열이다.
#
#   이 문자열 안에는 참고 문서와 사용자 질문이 함께 포함된다.
def build_prompt(question: str, search_results: List[Dict]) -> str:
    """질문과 검색 문서를 결합하여 RAG Prompt를 생성한다."""

    ######################################################
    # build_context()
    ######################################################

    # - 기능 및 역할:
    #
    #   검색 결과 목록을 LLM이 읽기 쉬운 참고 문서 문자열로 변환한다.
    #
    # - 파라미터:
    #
    #   search_results
    #
    #   Vector DB 검색 결과 목록이다.
    #
    # - 리턴 타입:
    #
    #   str
    #
    # - 리턴값:
    #
    #   여러 검색 문서가 하나로 합쳐진 Context 문자열이다.
    context = build_context(search_results)

    ######################################################
    # prompt
    ######################################################

    # - 기능 및 역할:
    #
    #   LLM에게 전달할 최종 입력 문장을 만든다.
    #
    #   이 Prompt 안에는 AI의 역할, 답변 규칙, 참고 문서, 사용자 질문이 포함된다.
    #
    # - f-string 사용 이유:
    #
    #   {context} 위치에 검색 문서 문자열을 넣고,
    #   {question} 위치에 사용자 질문을 넣기 위해 사용한다.
    #
    # - 삼중 따옴표 문자열:
    #
    #   여러 줄 문자열을 작성할 때 사용한다.
    #
    #   일반 따옴표(" ")는 한 줄 문자열에 주로 사용하고,
    #   삼중 따옴표(""" """)는 여러 줄 문자열에 주로 사용한다.
    prompt = f"""
당신은 사내 AI Data Platform 연구 프로젝트를 지원하는 AI Assistant입니다.
아래의 참고 문서를 기반으로 사용자의 질문에 답변하세요.

답변 규칙:
1. 참고 문서에 근거하여 답변합니다.
2. 참고 문서에 없는 내용은 추측하지 않습니다.
3. 답변은 이해하기 쉽게 정리합니다.
4. 필요한 경우 항목으로 구분하여 설명합니다.

[참고 문서]
{context}

[사용자 질문]
{question}

[답변]
""".strip()

    ######################################################
    # strip()
    ######################################################

    # - 기능 및 역할:
    #
    #   문자열의 앞뒤 공백과 줄바꿈을 제거한다.
    #
    #   여기서는 삼중 따옴표 문자열 작성 과정에서 생기는
    #   맨 앞과 맨 뒤의 불필요한 줄바꿈을 제거하기 위해 사용한다.
    #
    # - 파라미터:
    #
    #   없음
    #
    # - 리턴 타입:
    #
    #   str
    #
    # - 리턴값:
    #
    #   앞뒤 공백이 제거된 새로운 문자열이다.
    #
    # - 참고:
    #
    #   strip()은 원본 문자열을 직접 수정하지 않고,
    #   공백이 제거된 새 문자열을 반환한다.

    ######################################################
    # return
    ######################################################

    # - 기능 및 역할:
    #
    #   build_prompt() 함수의 최종 결과를 호출한 곳으로 반환한다.
    #
    # - 리턴 타입:
    #
    #   str
    #
    # - 리턴값:
    #
    #   LLM에게 전달할 최종 Prompt 문자열이다.
    return prompt


