#
#   반대로 다른 파일에서 이 파일을 import할 때는
#   아래 테스트 실행 코드가 자동 실행되지 않는다.
#
# - __name__ 의미:
#
#   파이썬이 자동으로 만들어주는 특수 변수이다.
#
#   파일을 직접 실행하면 __name__ 값은 "__main__" 이 된다.
#
#   다른 파일에서 import되면 __name__ 값은 모듈 이름이 된다.
#
# - 사용 이유:
#
#   함수는 다른 파일에서 재사용할 수 있게 두고,
#   테스트 실행 코드는 직접 실행할 때만 동작하게 하기 위해 사용한다.
if __name__ == "__main__":

    ######################################################
    # user_query
    ######################################################

    # - 기능 및 역할:
    #
    #   테스트용 사용자 질문을 저장한다.
    #
    #   실제 서비스에서는 사용자가 입력한 질문이 이 값으로 들어온다고 보면 된다.
    #
    # - 타입:
    #
    #   str
    #
    # - 값:
    #
    #   "MicroServer 프레임워크의 주요 구성요소는 무엇인가?"
    user_query = "MicroServer 프레임워크의 주요 구성요소는 무엇인가?"

    ######################################################
    # search_documents()
    ######################################################

    # - 기능 및 역할:
    #
    #   사용자 질문과 유사한 문서를 Vector DB에서 검색한다.
    #
    #   이 함수는 05_search_documents.py 파일에서 가져온 함수이다.
    #
    # - 파라미터:
    #
    #   1. user_query
    #
    #      검색 기준이 되는 사용자 질문이다.
    #
    #   2. top_k=3
    #
    #      검색 결과를 최대 3개 가져오겠다는 의미이다.
    #
    # - 리턴 타입:
    #
    #   list[dict]
    #
    # - 리턴값:
    #
    #   검색된 문서 목록이다.
    #
    #   예)
    #   [
    #       {
    #           "rank": 1,
    #           "document": "문서 내용",
    #           "metadata": {"source": "guide.md"},
    #           "distance": 0.12
    #       }
    #   ]
    search_results = search_documents(user_query, top_k=3)

    ######################################################
    # build_prompt()
    ######################################################

    # - 기능 및 역할:
    #
    #   사용자 질문과 검색 결과를 조합하여
    #   LLM에게 전달할 최종 Prompt를 생성한다.
    #
    # - 파라미터:
    #
    #   1. user_query
    #
    #      사용자 질문 문자열이다.
    #
    #   2. search_results
    #
    #      Vector DB에서 검색된 문서 목록이다.
    #
    # - 리턴 타입:
    #
    #   str
    #
    # - 리턴값:
    #
    #   LLM에게 전달할 Prompt 문자열이다.
    prompt = build_prompt(user_query, search_results)

    ######################################################
    # print()
    ######################################################

    # - 기능 및 역할:
    #
    #   괄호 안에 전달한 값을 콘솔 화면에 출력한다.
    #
    # - 파라미터:
    #
    #   prompt
    #
    #   출력할 문자열이다.
    #
    # - 리턴 타입:
    #
    #   None
    #
    # - 리턴값:
    #
    #   print() 함수는 값을 반환하지 않는다.
    #   대신 콘솔 화면에 문자열을 출력한다.
    print(prompt)
