"""
Step2-3. RAG 질의응답 구현 - 07_ask_llm.py

역할:
- 생성된 Prompt를 로컬 LLM(Ollama)에 전달한다.
- LLM으로부터 답변을 받아 출력한다.

사전 조건:
- Ollama가 실행 중이어야 한다.
- 사용할 모델이 로컬에 설치되어 있어야 한다.

모델 확인:
    ollama list

실행 예시:
    python labs/rag/07_ask_llm.py

초보자 관점 설명:
- 이 파일은 Python 코드에서 HTTP 요청을 보내는 예제이다.
- 사용자가 만든 Prompt 문자열을 Ollama API에 POST 방식으로 전달한다.
- Ollama는 전달받은 Prompt를 LLM 모델에 입력하고 답변을 생성한다.
- 생성된 답변은 JSON 형태로 돌아오며, 그중 "response" 값을 꺼내서 반환한다.
"""

######################################################
# import
######################################################

# - 기능 및 역할:
#
#   Python에서 외부 모듈 또는 표준 라이브러리를 현재 파일에서 사용할 수 있도록 가져온다.
#
# - 현재 코드에서 사용하는 모듈:
#
#   1. json
#      Python dict 객체와 JSON 문자열을 서로 변환할 때 사용한다.
#
#   2. urllib.request
#      HTTP 요청을 생성하고 서버로 전송할 때 사용한다.
#
#   3. urllib.error
#      HTTP 요청 중 발생하는 URL 관련 예외를 처리할 때 사용한다.
import json
import urllib.request
import urllib.error


######################################################
# OLLAMA_URL
######################################################

# - 기능 및 역할:
#
#   Ollama 로컬 서버의 LLM 생성 API 주소를 저장한다.
#
# - 값 설명:
#
#   http://localhost:11434
#
#   현재 PC에서 실행 중인 Ollama 서버 주소이다.
#
#   /api/generate
#
#   Ollama에서 Prompt를 전달받아 답변을 생성하는 API 경로이다.
#
# - 타입:
#
#   str
OLLAMA_URL = "http://localhost:11434/api/generate"


######################################################
# OLLAMA_MODEL
######################################################

# - 기능 및 역할:
#
#   Ollama에서 사용할 기본 LLM 모델명을 저장한다.
#
# - 값 설명:
#
#   gemma3:4b
#
#   Ollama에 설치되어 있는 모델 이름이다.
#
# - 확인 방법:
#
#   터미널에서 아래 명령어로 설치된 모델 목록을 확인할 수 있다.
#
#   ollama list
#
# - 타입:
#
#   str
OLLAMA_MODEL = "gemma3:4b"


######################################################
# ask_llm()
######################################################

# - 기능 및 역할:
#
#   사용자가 만든 Prompt 문자열을 Ollama 로컬 LLM에 전달하고,
#   LLM이 생성한 답변 문자열을 반환한다.
#
#   즉, Python 코드에서 Ollama API를 호출하는 핵심 함수이다.
#
# - 파라미터:
#
#   1. prompt: str
#
#      LLM에게 전달할 질문 또는 지시문이다.
#
#      예)
#      "RAG가 무엇인지 한 문단으로 설명해줘."
#
#   2. model: str = OLLAMA_MODEL
#
#      사용할 Ollama 모델명이다.
#
#      값을 직접 전달하지 않으면 기본값으로 OLLAMA_MODEL 값을 사용한다.
#
#      현재 기본값:
#      "gemma3:4b"
#
# - 리턴 타입:
#
#   str
#
# - 리턴값:
#
#   Ollama LLM이 생성한 답변 문자열을 반환한다.
#
#   예)
#   "RAG는 검색 증강 생성 방식으로..."
#
# - 예외:
#
#   Ollama 서버에 연결할 수 없으면 RuntimeError를 발생시킨다.
def ask_llm(prompt: str, model: str = OLLAMA_MODEL) -> str:
    """Ollama 로컬 LLM에 Prompt를 전달하고 답변을 반환한다."""

    ######################################################
    # payload
    ######################################################

    # - 기능 및 역할:
    #
    #   Ollama API에 전달할 요청 데이터를 Python dict 형태로 만든다.
    #
    #   HTTP 요청을 보낼 때 이 dict를 JSON 문자열로 변환해서 전송한다.
    #
    # - 구성 항목:
    #
    #   1. model
    #
    #      사용할 LLM 모델명이다.
    #
    #   2. prompt
    #
    #      LLM에게 전달할 질문 또는 지시문이다.
    #
    #   3. stream
    #
    #      응답을 스트리밍 방식으로 받을지 여부이다.
    #
    #      False이면 답변이 모두 생성된 후 한 번에 받는다.
    #
    # - 타입:
    #
    #   dict[str, Any]
    payload = {
        "model": model,
        "prompt": prompt,
        "stream": False,
    }

    ######################################################
    # json.dumps()
    ######################################################

    # - 기능 및 역할:
    #
    #   Python dict 객체를 JSON 문자열로 변환한다.
    #
    #   HTTP API는 보통 Python dict를 직접 받을 수 없고,
    #   JSON 형식의 문자열 데이터를 받는다.
    #
    # - 파라미터:
    #
    #   payload
    #
    #   JSON 문자열로 변환할 Python dict 객체이다.
    #
    # - 리턴 타입:
    #
    #   str
    #
    # - 리턴값 예시:
    #
    #   '{"model": "gemma3:4b", "prompt": "질문", "stream": false}'
    #
    ######################################################
    # encode()
    ######################################################
    #
    # - 기능 및 역할:
    #
    #   문자열을 바이트(bytes) 데이터로 변환한다.
    #
    #   HTTP 요청의 body 데이터는 문자열이 아니라 bytes 형태로 전달해야 한다.
    #
    # - 파라미터:
    #
    #   "utf-8"
    #
    #   문자열을 바이트로 변환할 때 사용할 문자 인코딩 방식이다.
    #
    # - 리턴 타입:
    #
    #   bytes
    data = json.dumps(payload).encode("utf-8")

    ######################################################
    # urllib.request.Request()
    ######################################################

    # - 기능 및 역할:
    #
    #   HTTP 요청 객체를 생성한다.
    #
    #   여기서는 Ollama API에 POST 방식으로 JSON 데이터를 보내기 위한 요청을 만든다.
    #
    # - 파라미터:
    #
    #   1. OLLAMA_URL
    #
    #      요청을 보낼 API 주소이다.
    #
    #      예)
    #      "http://localhost:11434/api/generate"
    #
    #   2. data=data
    #
    #      서버로 전송할 요청 본문(body) 데이터이다.
    #
    #      json.dumps(payload).encode("utf-8") 결과이다.
    #
    #   3. headers={"Content-Type": "application/json"}
    #
    #      요청 데이터가 JSON 형식이라는 것을 서버에 알려준다.
    #
    #   4. method="POST"
    #
    #      HTTP 요청 방식을 지정한다.
    #
    #      POST는 서버에 데이터를 전달하여 처리 결과를 받는 방식이다.
    #
    # - 리턴 타입:
    #
    #   urllib.request.Request
    #
    # - 리턴값:
    #
    #   urlopen() 함수에 전달할 수 있는 HTTP 요청 객체를 반환한다.
    request = urllib.request.Request(
        OLLAMA_URL,
        data=data,
        headers={"Content-Type": "application/json"},
        method="POST",
    )

    ######################################################
    # try / except
    ######################################################

    # - 기능 및 역할:
    #
    #   오류가 발생할 수 있는 코드를 안전하게 실행하기 위한 예외 처리 문법이다.
    #
    #   try 블록 안의 코드를 실행하다가 오류가 발생하면
    #   except 블록에서 해당 오류를 처리한다.
    #
    # - 현재 코드에서 처리하는 상황:
    #
    #   Ollama 서버가 실행 중이 아니거나,
    #   URL 연결에 실패하는 경우를 처리한다.
    try:

        ######################################################
        # urllib.request.urlopen()
        ######################################################

        # - 기능 및 역할:
        #
        #   실제로 HTTP 요청을 서버에 전송하고 응답을 받는다.
        #
        #   여기서는 위에서 만든 request 객체를 Ollama 서버로 보낸다.
        #
        # - 파라미터:
        #
        #   1. request
        #
        #      urllib.request.Request()로 만든 HTTP 요청 객체이다.
        #
        #   2. timeout=120
        #
        #      응답을 최대 120초까지 기다린다는 의미이다.
        #
        #      120초 안에 응답이 오지 않으면 오류가 발생한다.
        #
        # - 리턴 타입:
        #
        #   HTTPResponse 객체
        #
        # - 리턴값:
        #
        #   서버가 반환한 응답 객체이다.
        #
        # - with 문 사용 이유:
        #
        #   응답 처리가 끝난 뒤 네트워크 연결 자원을 자동으로 정리하기 위해 사용한다.
        with urllib.request.urlopen(request, timeout=120) as response:

            ######################################################
            # response.read()
            ######################################################

            # - 기능 및 역할:
            #
            #   Ollama 서버가 반환한 응답 본문(body)을 읽는다.
            #
            # - 파라미터:
            #
            #   없음
            #
            # - 리턴 타입:
            #
            #   bytes
            #
            # - 리턴값:
            #
            #   JSON 문자열이 bytes 형태로 반환된다.
            #
            #   예)
            #   b'{"response": "RAG는 ..."}'
            #
            ######################################################
            # decode()
            ######################################################
            #
            # - 기능 및 역할:
            #
            #   bytes 데이터를 문자열(str)로 변환한다.
            #
            # - 파라미터:
            #
            #   "utf-8"
            #
            #   bytes를 문자열로 변환할 때 사용할 문자 인코딩 방식이다.
            #
            # - 리턴 타입:
            #
            #   str
            response_body = response.read().decode("utf-8")

            ######################################################
            # json.loads()
            ######################################################

            # - 기능 및 역할:
            #
            #   JSON 문자열을 Python dict 객체로 변환한다.
            #
            # - 파라미터:
            #
            #   response_body
            #
            #   Ollama 서버가 반환한 JSON 문자열이다.
            #
            # - 리턴 타입:
            #
            #   dict
            #
            # - 리턴값 예시:
            #
            #   {
            #       "model": "gemma3:4b",
            #       "created_at": "2026-06-22T...",
            #       "response": "RAG는 검색 증강 생성 방식입니다.",
            #       "done": True
            #   }
            result = json.loads(response_body)

            ######################################################
            # dict.get()
            ######################################################

            # - 기능 및 역할:
            #
            #   딕셔너리에서 지정한 Key의 값을 조회한다.
            #
            # - 역할:
            #
            #   "response" Key가 존재하면 해당 값을 반환하고,
            #   존재하지 않으면 기본값("")을 반환한다.
            #
            # - 파라미터:
            #
            #   1. "response"
            #
            #      조회할 Key 이름
            #
            #   2. ""
            #
            #      Key가 없을 경우 반환할 기본값(Default Value)
            #
            # - 리턴 타입:
            #
            #   str
            #
            # - 리턴값:
            #
            #   Ollama LLM이 생성한 답변 문자열
            #
            # - 실제 반환 예시:
            #
            #   "RAG는 검색 증강 생성 방식입니다."
            return result.get("response", "")

    ######################################################
    # urllib.error.URLError
    ######################################################

    # - 기능 및 역할:
    #
    #   URL 요청 과정에서 발생한 오류를 표현하는 예외 클래스이다.
    #
    # - 주로 발생하는 경우:
    #
    #   1. Ollama 서버가 실행 중이 아닌 경우
    #   2. localhost:11434 주소에 연결할 수 없는 경우
    #   3. 네트워크 요청이 실패한 경우
    #
    # - 현재 코드의 역할:
    #
    #   URL 연결 오류가 발생하면 사용자가 이해하기 쉬운 메시지로 RuntimeError를 발생시킨다.
    except urllib.error.URLError as error:

        ######################################################
        # RuntimeError
        ######################################################

        # - 기능 및 역할:
        #
        #   프로그램 실행 중 발생한 문제를 명확하게 알려주기 위한 예외이다.
        #
        # - 현재 코드의 역할:
        #
        #   Ollama 서버 연결 실패 원인을 사용자에게 설명한다.
        #
        # - from error 의미:
        #
        #   원래 발생한 URLError를 RuntimeError의 원인으로 연결한다.
        #   이렇게 하면 디버깅할 때 실제 원인도 함께 추적할 수 있다.
        raise RuntimeError(
            "Ollama 서버에 연결할 수 없습니다. "
            "'ollama serve' 또는 Ollama 앱 실행 상태를 확인하세요."
        ) from error


######################################################
# ask_llm_with_simple_prompt()
######################################################

# - 기능 및 역할:
#
#   LLM 연결 상태를 확인하기 위한 단순 테스트 함수이다.
#
#   고정된 Prompt를 ask_llm() 함수에 전달하고,
#   Ollama에서 생성한 답변을 반환한다.
#
# - 파라미터:
#
#   없음
#
# - 리턴 타입:
#
#   str
#
# - 리턴값:
#
#   ask_llm() 함수가 반환한 LLM 답변 문자열
#
# - 사용 목적:
#
#   RAG 전체 흐름을 연결하기 전에
#   Python 코드에서 Ollama 호출이 정상적으로 되는지 확인한다.
def ask_llm_with_simple_prompt():
    """LLM 연결 상태를 확인하기 위한 단순 테스트 함수."""

    ######################################################
    # prompt
    ######################################################

    # - 기능 및 역할:
    #
    #   LLM에게 전달할 테스트 질문을 문자열로 저장한다.
    #
    # - 타입:
    #
    #   str
    prompt = "RAG가 무엇인지 한 문단으로 설명해줘."

    ######################################################
    # ask_llm()
    ######################################################

    # - 기능 및 역할:
    #
    #   위에서 정의한 prompt를 Ollama LLM에 전달하고 답변을 받는다.
    #
    # - 파라미터:
    #
    #   prompt
    #
    #   LLM에게 전달할 질문 문자열
    #
    # - 리턴 타입:
    #
    #   str
    #
    # - 리턴값:
    #
    #   LLM이 생성한 답변 문자열
    answer = ask_llm(prompt)

    ######################################################
    # return
    ######################################################

    # - 기능 및 역할:
    #
    #   함수 실행 결과를 호출한 곳으로 돌려준다.
    #
    # - 리턴 타입:
    #
    #   str
    #
    # - 리턴값:
    #
    #   answer 변수에 저장된 LLM 답변 문자열
    return answer


######################################################
# if __name__ == "__main__"
######################################################

# - 기능 및 역할:
#
#   현재 파일이 직접 실행되었을 때만 아래 코드를 실행하도록 제어한다.
#
# - 의미:
#
#   python 07_ask_llm.py
#
#   위처럼 직접 실행하면 __name__ 값이 "__main__"이 된다.
#
#   다른 Python 파일에서 import해서 사용할 때는
#   아래 테스트 코드가 자동 실행되지 않는다.
#
# - 사용 이유:
#
#   함수는 다른 파일에서 재사용할 수 있게 하고,
#   테스트 실행 코드는 직접 실행할 때만 동작하게 분리하기 위해 사용한다.
if __name__ == "__main__":

    ######################################################
    # print()
    ######################################################

    # - 기능 및 역할:
    #
    #   값을 화면, 즉 터미널에 출력한다.
    #
    # - 파라미터:
    #
    #   출력할 값
    #
    # - 리턴 타입:
    #
    #   None
    #
    # - 리턴값:
    #
    #   없음
    print("Ollama LLM 호출 테스트")

    ######################################################
    # 문자열 곱셈
    ######################################################

    # - 기능 및 역할:
    #
    #   Python에서는 문자열에 숫자를 곱하면
    #   해당 문자열을 숫자만큼 반복한 새 문자열이 만들어진다.
    #
    # - 현재 코드 의미:
    #
    #   "=" 문자를 80번 반복해서 구분선을 출력한다.
    #
    # - 결과 예시:
    #
