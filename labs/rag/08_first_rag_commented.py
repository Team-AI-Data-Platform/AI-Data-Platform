######################################################
# Path(__file__).resolve().parent
######################################################

# - 기능 및 역할:
#
#   현재 파일이 위치한 디렉터리 경로를 구한다.
#
# - 처리 순서:
#
#   1. Path(__file__)
#      현재 파일 경로를 Path 객체로 변환한다.
#
#   2. resolve()
#      상대 경로를 절대 경로로 변환한다.
#
#   3. parent
#      파일명이 아니라 해당 파일이 들어있는 폴더 경로를 가져온다.
