    ######################################################

    # - 기능 및 역할:
    #
    #   사용자로부터 콘솔 입력을 받는다.
    #
    # - 파라미터:
    #
    #   "질문을 입력하세요: "
    #
    #   사용자 입력 전에 화면에 보여줄 안내 문구
    #
    # - 리턴 타입:
    #
    #   str
    #
    # - 리턴값:
    #
    #   사용자가 입력한 문자열
    #
    # - 현재 코드 의미:
    #
    #   사용자의 질문을 입력받는다.
    #
    #   이후 strip()을 사용하여 앞뒤 공백을 제거한다.

    ######################################################
    # strip()
    ######################################################

    # - 기능 및 역할:
    #
    #   문자열의 앞뒤 공백, 탭, 줄바꿈 문자를 제거한다.
    #
    # - 파라미터:
    #
    #   없음
    #
    # - 리턴 타입:
    #
    #   str
    #
    # - 리턴값:
    #
    #   앞뒤 공백이 제거된 새로운 문자열
    #
    # - 예시:
    #
    #   "  안녕하세요  ".strip()
    #
    #   결과:
    #       "안녕하세요"
    #
    # - 주의:
    #
    #   문자열 중간에 있는 공백은 제거하지 않는다.
    #
    #   예)
