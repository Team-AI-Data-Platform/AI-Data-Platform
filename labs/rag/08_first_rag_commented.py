######################################################

# - 기능 및 역할:
#
#   Python이 import할 모듈을 찾는 경로 목록에 새 경로를 추가한다.
#
#   Python은 import를 실행할 때 sys.path에 등록된 경로들을 확인하면서
#   해당 모듈 파일을 찾는다.
#
# - 파라미터:
#
#   str(BASE_DIR)
#
#   BASE_DIR은 Path 객체이므로 문자열 경로로 변환해서 추가한다.
#
# - 리턴 타입:
#
#   None
#
# - 리턴값:
#
#   append()는 값을 반환하지 않는다.
#   대신 sys.path 목록 자체가 변경된다.
#
# - 현재 코드 의미:
#
#   현재 파일이 있는 labs/rag 디렉터리를 Python 모듈 검색 경로에 추가한다.
#
#   이렇게 해야 같은 폴더에 있는
#   05_search_documents.py,
#   06_build_prompt.py,
#   07_ask_llm.py 파일을 불러올 수 있다.
sys.path.append(str(BASE_DIR))


######################################################
# import_module()
######################################################

# - 기능 및 역할:
#
#   문자열로 전달한 모듈 이름을 기준으로 Python 모듈을 동적으로 import한다.
#
#   일반적인 import 문은 모듈 이름이 코드에 직접 작성되어야 한다.
#
#   예)
#       import my_module
#
#   하지만 import_module()은 문자열로 모듈명을 전달할 수 있다.
#
#   예)
#       import_module("my_module")
#
# - 파라미터:
#
#   모듈 이름 문자열
#
#   예)
#       "05_search_documents"
#       "06_build_prompt"
#       "07_ask_llm"
#
# - 리턴 타입:
#
#   module
#
# - 리턴값:
#
#   import된 Python 모듈 객체
#
# - 현재 코드에서 import_module()을 사용하는 이유:
#
#   Python 문법상 일반 import 문에서는 모듈명이 숫자로 시작하면 문제가 될 수 있다.
#
#   예)
#       import 05_search_documents
#
#   위 코드는 문법 오류가 발생할 수 있다.
#
#   그래서 문자열 기반 import_module()을 사용하여
#   숫자로 시작하는 파일명을 가진 실습 파일을 안전하게 불러온다.


######################################################
# search_module
######################################################

# - 기능 및 역할:
#
#   05_search_documents.py 파일을 모듈로 불러온 결과를 저장한다.
#
# - 리턴 타입:
#
#   module
#
# - 리턴값:
#
#   05_search_documents.py 안에 정의된 함수와 변수를 사용할 수 있는 모듈 객체
search_module = import_module("05_search_documents")


######################################################
# prompt_module
######################################################

# - 기능 및 역할:
#
#   06_build_prompt.py 파일을 모듈로 불러온 결과를 저장한다.
#
# - 리턴 타입:
#
#   module
#
# - 리턴값:
#
#   06_build_prompt.py 안에 정의된 함수와 변수를 사용할 수 있는 모듈 객체
prompt_module = import_module("06_build_prompt")


######################################################
# llm_module
######################################################

# - 기능 및 역할:
#
#   07_ask_llm.py 파일을 모듈로 불러온 결과를 저장한다.
#
# - 리턴 타입:
#
#   module
#
# - 리턴값:
#
#   07_ask_llm.py 안에 정의된 함수와 변수를 사용할 수 있는 모듈 객체
llm_module = import_module("07_ask_llm")


######################################################
# search_documents
######################################################

# - 기능 및 역할:
#
#   search_module 안에 있는 search_documents 함수를 현재 파일에서 바로 사용할 수 있도록
#   변수에 할당한다.
#
#   원래는 아래처럼 호출해야 한다.
#
#       search_module.search_documents(question, top_k=3)
#
#   하지만 아래처럼 함수만 따로 꺼내두면 더 간단하게 호출할 수 있다.
#
#       search_documents(question, top_k=3)
#
# - 함수 원본 위치:
#
#   05_search_documents.py
#
# - 기능:
#
#   사용자 질문과 유사한 문서 Chunk를 Chroma Vector DB에서 검색한다.
search_documents = search_module.search_documents


######################################################
# build_prompt
######################################################

# - 기능 및 역할:
#
#   prompt_module 안에 있는 build_prompt 함수를 현재 파일에서 바로 사용할 수 있도록
#   변수에 할당한다.
#
# - 함수 원본 위치:
#
#   06_build_prompt.py
#
# - 기능:
#
#   사용자 질문과 검색 결과를 기반으로 LLM에게 전달할 Prompt 문자열을 생성한다.
build_prompt = prompt_module.build_prompt


######################################################
# ask_llm
######################################################

# - 기능 및 역할:
#
#   llm_module 안에 있는 ask_llm 함수를 현재 파일에서 바로 사용할 수 있도록
#   변수에 할당한다.
#
# - 함수 원본 위치:
#
#   07_ask_llm.py
#
# - 기능:
#
#   생성된 Prompt를 Ollama LLM에 전달하고 답변 문자열을 반환한다.
ask_llm = llm_module.ask_llm


######################################################
# run_rag()
######################################################

# - 기능 및 역할:
#
#   RAG 질의응답 전체 흐름을 실행한다.
#
#   이 함수는 아래 3단계를 순서대로 수행한다.
#
#   1. 질문과 관련된 문서를 Vector DB에서 검색한다.
#   2. 검색 결과를 포함한 Prompt를 생성한다.
#   3. Prompt를 LLM에 전달하여 최종 답변을 생성한다.
#
# - 파라미터:
#
#   1. question: str
#
#      사용자가 입력한 질문 문자열
#
#      예)
#          "MicroServer 프레임워크의 주요 구성요소는 무엇인가?"
#
#   2. top_k: int = 3
#
#      Vector DB에서 가져올 유사 문서 개수
#
#      기본값은 3이다.
#
#      즉, 함수 호출 시 top_k 값을 따로 넘기지 않으면 3개 문서를 검색한다.
#
# - 리턴 타입:
#
#   str
#
# - 리턴값:
#
#   LLM이 생성한 최종 답변 문자열
#
# - 전체 흐름:
#
#   question
#      ↓
#   search_documents()
#      ↓
#   search_results
#      ↓
#   build_prompt()
#      ↓
#   prompt
#      ↓
#   ask_llm()
#      ↓
#   answer
#
# - 현재 코드 의미:
#
#   검색, 프롬프트 생성, LLM 답변 생성을 하나의 함수로 묶어
#   외부에서는 run_rag(question)만 호출하면 되도록 만든다.
def run_rag(question: str, top_k: int = 3) -> str:
    """검색부터 답변 생성까지 End-to-End RAG 흐름을 실행한다."""

    ######################################################
    # search_documents()
    ######################################################

    # - 기능 및 역할:
    #
    #   사용자 질문을 임베딩 벡터로 변환한 뒤,
    #   Chroma Vector DB에서 질문과 유사한 문서 Chunk를 검색한다.
    #
    # - 파라미터:
    #
    #   1. question
    #      사용자 질문 문자열
    #
    #   2. top_k=top_k
    #      검색할 문서 개수
    #
    # - 리턴 타입:
    #
    #   list[dict[str, Any]]
    #
    # - 리턴값 예시:
    #
    #   [
    #       {
    #           "rank": 1,
    #           "document": "검색된 문서 내용",
    #           "metadata": {"source": "microserver_guide.md"},
    #           "distance": 0.12
    #       },
    #       {
    #           "rank": 2,
    #           "document": "검색된 문서 내용",
    #           "metadata": {"source": "microserver_guide.md"},
    #           "distance": 0.18
    #       }
    #   ]
    search_results = search_documents(question, top_k=top_k)

    ######################################################
    # build_prompt()
    ######################################################

    # - 기능 및 역할:
    #
    #   사용자 질문과 Vector DB 검색 결과를 조합하여
    #   LLM이 답변하기 좋은 Prompt 문자열을 만든다.
    #
    # - 파라미터:
    #
    #   1. question
    #      사용자 질문 문자열
    #
    #   2. search_results
    #      search_documents()가 반환한 검색 결과 목록
    #
    # - 리턴 타입:
    #
    #   str
    #
    # - 리턴값:
    #
    #   LLM에 전달할 최종 Prompt 문자열
    #
    # - 현재 코드 의미:
    #
    #   검색된 문서를 Context로 포함하고,
    #   사용자의 질문을 Question 영역에 포함한 Prompt를 생성한다.
    prompt = build_prompt(question, search_results)

    ######################################################
    # ask_llm()
    ######################################################

    # - 기능 및 역할:
    #
    #   생성된 Prompt를 Ollama LLM API에 전달하고
    #   LLM이 생성한 답변을 받아온다.
    #
    # - 파라미터:
    #
    #   prompt
    #
    #   LLM에게 전달할 Prompt 문자열
    #
    # - 리턴 타입:
    #
    #   str
    #
    # - 리턴값:
    #
    #   LLM이 생성한 답변 문자열
    answer = ask_llm(prompt)

    ######################################################
    # return
    ######################################################

    # - 기능 및 역할:
    #
    #   run_rag() 함수의 최종 결과를 호출한 쪽으로 반환한다.
    #
    # - 리턴 타입:
    #
    #   str
    #
    # - 리턴값:
    #
    #   LLM이 생성한 RAG 최종 답변
    return answer


